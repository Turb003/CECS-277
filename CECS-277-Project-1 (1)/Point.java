/**
* Point class that contains x, y coordinates
*
*
*/

public class Point
{
  public int x;
  public int y;

  /**
	 * Constructor that initializes object with x,y coordaintes
	 * 
	 * @param x   current x coordinate
   * @param y   current y coordinate
	 */
  public Point(int x, int y)
  {
    this.x = x;
    this.y = y;
  }
}